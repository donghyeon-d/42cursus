#include "libft.h"

int	ft_isalnum(int c)
{
	if ('0' <= c && c <= '9')
		return (1);
	else if ('A' <= c && c <= 'Z')
		return (2);
	else if ('a' <= c && c <= 'z')
		return (3);
	else
		return (0);
}
