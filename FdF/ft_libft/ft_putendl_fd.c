#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	n;

	n = 0;
	while (s[n])
		n++;
	if (n != 0)
	{
		write(fd, s, n);
		write(fd, "\n", 1);
	}
}
